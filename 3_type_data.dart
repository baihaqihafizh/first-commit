/**
 * Variabel adalah suatu wadah untuk
 * menyimpan data  
 */ 

/**
 * Tipe data adalah suatu konsep yang digunakan
 * untuk mengklasifikasikan jenis data
 * yang dapat diproses oleh progam
 */

/**
 * jenis tipe data
 * 
 * int => integer => bilangan bulat 
 * contoh 1, -2, 11 dsb / dan sebagainya
 */

/**
 * double => bilangan desimal
 * contoh => 3.14, -0.1, 2.5 dsb / dan sebagainya
 */

/**
 * num => gabungan dari integer dan double
 * 3.14, 2, -2, 0, 3.15 dsb / dan sebagainya
 */

/**
 * bool = boolean = true or false
 */

/**
 * String =  string => "kumpulan karakter"
 */

/**
 * List = berupa daftar
 * [1, 2 dsb ]
 * [a' b' dsb]
 */

/**
 * Map => berupa key:value
 * {"nama kota : solo"}
 */

/**
 * Dynamic => campuran / tipe apapun
 */

